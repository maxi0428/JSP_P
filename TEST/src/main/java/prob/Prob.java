package prob;

public class Prob {
	
	private int probID;
	private String probTitle;
	private String userID;
	private String probDate;
	private String probContent;
	private String probSt;
	private int probAvailable;
	
	
	
	public int getProbAvailable() {
		return probAvailable;
	}
	public void setProbAvailable(int probAvailable) {
		this.probAvailable = probAvailable;
	}
	public int getProbID() {
		return probID;
	}
	public void setProbID(int probID) {
		this.probID = probID;
	}
	public String getProbTitle() {
		return probTitle;
	}
	public void setProbTitle(String probTitle) {
		this.probTitle = probTitle;
	}
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getProbDate() {
		return probDate;
	}
	public void setProbDate(String probDate) {
		this.probDate = probDate;
	}
	public String getProbContent() {
		return probContent;
	}
	public void setProbContent(String probContent) {
		this.probContent = probContent;
	}
	public String getProbSt() {
		return probSt;
	}
	public void setProbSt(String probSt) {
		this.probSt = probSt;
	}
		
	
	
}
